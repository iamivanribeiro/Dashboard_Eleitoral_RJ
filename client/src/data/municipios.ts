// Função para calcular estatísticas por região
export function getEstatisticasPorRegiao(regiao: string) {
  const municipiosDaRegiao = municipios.filter((m) => m.regiao === regiao);
  const totalVotos = municipiosDaRegiao.reduce(
    (sum, m) => sum + m.votosFlavio + m.votosCanella,
    0
  );
  const mediaSinergia =
    municipiosDaRegiao.reduce((sum, m) => sum + m.sinergia, 0) /
    municipiosDaRegiao.length;

  return {
    regiao,
    municipios: municipiosDaRegiao.length,
    totalVotos,
    mediaSinergia,
  };
}

// Função para buscar um município por ID
export function getMunicipioById(id: string): Municipio | undefined {
  return municipios.find((m) => m.id === id);
}

// Função para obter todas as regiões únicas
export function getRegioes(): string[] {
  return Array.from(new Set(municipios.map((m) => m.regiao))).sort();
}
